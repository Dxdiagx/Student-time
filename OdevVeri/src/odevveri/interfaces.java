int[] diziOlustur() {
int diziBoyutu= Integer.parseInt(DiziBoyutu.getText());

int[] dizi1 = new int[diziBoyutu];
Random r = new Random();
for (int i = 0; i < diziBoyutu; i++) {
dizi1[i] = r.nextInt(1000);
}
return dizi1;
}
    private void AlgoritmalarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlgoritmalarActionPerformed
   dizi1=diziOlustur();
        switch (Algoritmalar.getSelectedItem().toString()) {
    case "Selection Sort" :sıralDizi.setText(Arrays.toString(SıralamaAlgoritmaları.selectionSort(dizi1))); break;
    case "Insertion Sort" : sıralDizi.setText(Arrays.toString(SıralamaAlgoritmaları.insertionSort(dizi1))); break;
    case "Bubble Sort" : sıralDizi.setText(Arrays.toString(SıralamaAlgoritmaları.bubbleSort(dizi1))); break;
}  
    }//GEN-LAST:event_AlgoritmalarActionPerformed

