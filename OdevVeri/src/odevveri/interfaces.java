    private void CizdirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CizdirActionPerformed
  if (DiziBoyutu.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Lütfen dizi boyutu girin!");
        return;
    }
   if (dizi1==null) {
        JOptionPane.showMessageDialog(this, "Lütfen Bir Algoritma Seçin!");
        return;
    }
   ArrayList<Integer> adimSayilari = new ArrayList();
adimSayilari.add(SıralamaAlgoritmalarıForSteps.selectionSort(dizi1)); 
adimSayilari.add(SıralamaAlgoritmalarıForSteps.insertionSort(dizi1)); 
adimSayilari.add(SıralamaAlgoritmalarıForSteps.bubbleSort(dizi1));


// Grafiği oluşturmak için gerekli veri setini oluşturuyoruz
JFrame frame = new JFrame();

// Grafiklerin gösterileceği panel oluşturuyoruz
JPanel panel = new JPanel(new GridLayout(0, 3)); // Bu panel tüm grafikleri yatay olarak sıralayacak

// Grafiği oluşturmak için gerekli veri setini oluşturuyoruz
DefaultCategoryDataset dataset1 = new DefaultCategoryDataset();
dataset1.addValue(adimSayilari.get(0), "Adım Sayısı", "Selection Sort");
JFreeChart chart1 = ChartFactory.createBarChart("Selection Sort Adım Sayısı", "Algoritma", "Adım Sayısı", dataset1, PlotOrientation.VERTICAL, false, true, false);
ChartPanel chartPanel1 = new ChartPanel(chart1);
panel.add(chartPanel1); // Grafiği panel'e ekliyoruz

DefaultCategoryDataset dataset2 = new DefaultCategoryDataset();
dataset2.addValue(adimSayilari.get(1), "Adım Sayısı", "Insertion Sort");
JFreeChart chart2 = ChartFactory.createBarChart("Insertion Sort Adım Sayısı", "Algoritma", "Adım Sayısı", dataset2, PlotOrientation.VERTICAL, false, true, false);
ChartPanel chartPanel2 = new ChartPanel(chart2);
panel.add(chartPanel2); // Grafiği panel'e ekliyoruz

DefaultCategoryDataset dataset3 = new DefaultCategoryDataset();
dataset3.addValue(adimSayilari.get(2), "Adım Sayısı", "Bubble Sort");
JFreeChart chart3 = ChartFactory.createBarChart("Bubble Sort Adım Sayısı", "Algoritma", "Adım Sayısı", dataset3, PlotOrientation.VERTICAL, false, true, false);
ChartPanel chartPanel3 = new ChartPanel(chart3);
panel.add(chartPanel3); // Grafiği panel'e ekliyoruz
panel.setPreferredSize(new java.awt.Dimension(700, 367));
// Paneli frame'e ekleyelim
frame.add(panel);
frame.pack();
frame.setVisible(true);
    }//GEN-LAST:event_CizdirActionPerformed

